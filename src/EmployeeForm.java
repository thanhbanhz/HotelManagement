    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
            // Lấy thông tin từ các trường văn bản
        String name = txtNameEmployee.getText();
        String phoneNumber = txtPhoneNumber.getText();
        String email = txtEmail.getText();
        String gender = cbGenderEmployee.getSelectedItem().toString();
        String address = txtAddressEmployee.getText();
        String nationality = txtNationalityEmployee.getText();
        String usertype = cbPosition.getSelectedItem().toString();

        // Kiểm tra thông tin
        if (name.isEmpty() || phoneNumber.isEmpty() || email.isEmpty() || gender.isEmpty() ||
                address.isEmpty() || nationality.isEmpty() || usertype.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.");
        } else if (!phoneNumber.matches("^0\\d{8,9}$")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ. Phải bắt đầu bằng số 0 và có 9 hoặc 10 chữ số.");
        } else if (!email.endsWith("@gmail.com")) {
            JOptionPane.showMessageDialog(this, "Email không hợp lệ. Phải kết thúc bằng '@gmail.com'.");
        } else {
            // Thực hiện thêm nhân viên vào cơ sở dữ liệu
            try {
                Connection conn = getConnection();
                String insertQuery = "INSERT INTO employees (Name, PhoneNumber, Email, Gender, Address, Nationality, UserType) VALUES (?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement preparedStatement = conn.prepareStatement(insertQuery);

                preparedStatement.setString(1, name);
                preparedStatement.setString(2, phoneNumber);
                preparedStatement.setString(3, email);
                preparedStatement.setString(4, gender);
                preparedStatement.setString(5, address);
                preparedStatement.setString(6, nationality);
                preparedStatement.setString(7, usertype);

                int rowsAffected = preparedStatement.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Nhân viên đã được đăng ký thành công.");
                } else {
                    JOptionPane.showMessageDialog(this, "Lỗi khi đăng ký nhân viên.");
                }

                conn.close();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi kết nối CSDL: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnlistEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnlistEmployeeActionPerformed
        try {
            Connection conn = getConnection();
            String query = "SELECT * FROM employees"; // Thay đổi tên bảng nếu cần
            PreparedStatement preparedStatement = conn.prepareStatement(query);
            ResultSet result = preparedStatement.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tableList.getModel();
            model.setRowCount(0); // Xóa tất cả các dòng cũ trong bảng

            while (result.next()) {
                model.addRow(new Object[]{
                    result.getInt("EmployeeID"),
                    result.getString("Name"),
                    result.getString("PhoneNumber"),
                    result.getString("Email"),
                    result.getString("Gender"),
                    result.getString("Address"),
                    result.getString("Nationality"),
                    result.getString("UserType")
                });
            }

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi kết nối CSDL: " + e.getMessage());
        }
    }//GEN-LAST:event_btnlistEmployeeActionPerformed

