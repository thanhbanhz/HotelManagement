    private void btnDeletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletActionPerformed
        String employeeID = txtEmployeeID.getText();

        // Kiểm tra xem trường EmployeeID có giá trị hợp lệ không
        if (employeeID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập EmployeeID.");
            return;
        }

        // Xác nhận xóa bằng hỏi người dùng
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa nhân viên có EmployeeID = " + employeeID + "?", "Xác nhận Xóa", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            // Thực hiện xóa nhân viên từ cơ sở dữ liệu ở đây
            try {
                Connection conn = getConnection();
                String deleteQuery = "DELETE FROM employees WHERE EmployeeID = ?";
                PreparedStatement preparedStatement = conn.prepareStatement(deleteQuery);
                preparedStatement.setString(1, employeeID);

                int rowsAffected = preparedStatement.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Nhân viên đã được xóa thành công.");
                    txtEmployeeID.setText(""); // Xóa trường tìm kiếm
                } else {
                    JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên với EmployeeID = " + employeeID);
                }

                conn.close();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi kết nối CSDL: " + e.getMessage());
            }

            // Sau khi xóa, bạn có thể làm mới bảng hoặc hiển thị lại danh sách nhân viên.
        }
    }//GEN-LAST:event_btnDeletActionPerformed

